using System;
using System.Collections.Generic;
using System.Linq;
using System.Threading;
using System.Web;
using System.Web.Http;
using System.Web.Mvc;
using System.Web.Optimization;
using System.Web.Routing;

namespace WebApp
{
    public class MvcApplication : System.Web.HttpApplication
    {
        protected void Application_Start()
        {
            AreaRegistration.RegisterAllAreas();
            GlobalConfiguration.Configure(WebApiConfig.Register);
            FilterConfig.RegisterGlobalFilters(GlobalFilters.Filters);
            RouteConfig.RegisterRoutes(RouteTable.Routes);
            BundleConfig.RegisterBundles(BundleTable.Bundles);
        }

        protected void Application_AcquireRequestState(object sender, EventArgs eventArgs)
        {
            var checkLanguage = Request.Cookies["Lang"];
            if(checkLanguage != null)
            {
                var currentLanguage = checkLanguage.Value;
                Thread.CurrentThread.CurrentCulture = new System.Globalization.CultureInfo(currentLanguage);
                Thread.CurrentThread.CurrentUICulture = new System.Globalization.CultureInfo(currentLanguage);
            }

            //AreaRegistration.RegisterAllAreas();
